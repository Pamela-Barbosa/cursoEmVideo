altura = float(input("Digite a altura da parede: "))
largura = float(input("\nDigite a largura da parede: "))


area = altura * largura

tintaNecessaria = area / 2

print(f"\nAltura da parede: {altura}")
print(f"Largura da parede: {largura}")
print(f"Área da parede: {area}")
print(f"Quantidade de tinta necessária para pintar éssa parede é: {tintaNecessaria}")