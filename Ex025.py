frase = input("Digite uma frase: ")

print("Quantas letras A aparece na sua frase?\n",frase.count('a'))
print("Qual a posição da primeria letra A que aparece na frase?\n",frase.index('a'))
print("Qual a ultima posição da letra A que aparece na frase?\n",frase.rindex('a'))

#o rindex pega a ultima posição desejada